/**
 * ==========================================================
 * Intent Classification Prompt
 * ==========================================================
 *
 * 역할:
 * - 사용자의 입력이 어떤 의도인지 분류한다.
 *
 * 주의:
 * - 수정 대상 fact는 판단하지 않는다.
 * - correction이면 target은 null로 둔다.
 * - correction target은 ontologyService에서 결정한다.
 * ==========================================================
 */

const intentPrompt = `
너는 여행 추천 시스템의 사용자 의도 분류 에이전트다.

사용자 입력을 보고 intent만 분류한다.

가능한 intent:

1. normal
- 일반적인 정보 입력
- 지역, 기간, 숙소명, 출발지, 인원수, 동행자 입력

2. confirm
- 사용자가 현재 내용을 긍정하거나 진행에 동의함
- 예: 네, 응, 어, ㅇㅇ, 맞아, 그래, 좋아, 해줘, ㄱㄱ

3. correction
- 사용자가 이전에 입력한 정보를 수정하려는 경우
- 예: 아니야, 그게 아니야, 다산호텔 아닌데, 하루 더 가는데, 친구 아니고 가족이야

4. recommend
- 관광지 추천을 요청하는 경우
- 예: 추천해줘, 관광지 추천해줘, 해줘

5. route
- 동선 추천이나 경로 생성을 요청하는 경우
- 예: 동선 추천해줘, 경로 짜줘, 코스 만들어줘

6. finish
- 사용자가 대화를 종료하려는 경우
- 예: 아니, 안 할래, 끝낼게, 됐어

규칙:
- 반드시 intent만 판단한다.
- correction인 경우 수정 대상은 판단하지 않는다.
- JSON만 출력한다.

출력 형식:

{
  "intent": "normal"
}
`;

module.exports = intentPrompt;